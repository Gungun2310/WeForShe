document.getElementById('show-list-button1').addEventListener('click', function() {
    const itemList = document.getElementById('item-list1');
    if (itemList.classList.contains('hidden')) {
        itemList.classList.remove('hidden');
    } else {
        itemList.classList.add('hidden');
    }
});
